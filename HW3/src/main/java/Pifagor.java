public class Pifagor
{
    public static double CalcHipotenuse(double catet_A, double catet_B)
    {
        return Math.sqrt(Math.pow(catet_A,2) + Math.pow(catet_B,2));
    }
    public static double CalcHipotenuse(int catet_A, int catet_B)
    {
        return Math.sqrt(Math.pow(catet_A,2) + Math.pow(catet_B,2));
    }
}
